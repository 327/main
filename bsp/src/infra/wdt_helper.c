#include <watchdog.h>
static struct device *wdt_dev;

void wdt_start(uint32_t timeout_ms)
{
	struct wdt_config wdt_cfg;

	extern struct device DEVICE_NAME_GET(wdt);
	wdt_cfg.timeout = timeout_ms;
	wdt_cfg.mode = WDT_MODE_INTERRUPT_RESET;
	wdt_cfg.interrupt_fn = NULL;
	wdt_dev = DEVICE_GET(wdt);
	wdt_set_config(wdt_dev, &wdt_cfg);
}

void wdt_keepalive(void)
{
	wdt_reload(wdt_dev);
}
